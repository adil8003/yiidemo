            [['name', 'mode'], 'string', 'max' => 100]
        ];
    }

    /**
     * @inheritdoc
     */
    public function attributeLabels()
    {
        return [
            'id' => 'ID',
            'name' => 'Name',
            'mode' => 'Mode',
        ];
    }

    /**
     * @return \yii\db\ActiveQuery
